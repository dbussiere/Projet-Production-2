                        <div class="col-xs-12 col-sm-12 col-md-12 col-lg-12 text-article">

                            <p>Chaque année, de nombreuses classes du préscolaire, du primaire et du secondaire réalisent des projets qui visent à résoudre des problèmes ou à répondre à des besoins identifiés par les élèves auprès d’un large public cible.</p>
                            <p>Sous la forme de production de biens, de services ou d’événements, les élèves se mobilisent dans toutes les étapes de réalisation de leur projet. Ils collectent des données, planifient, élaborent, réalisent et évaluent leur projet entrepreneurial qui s’adressera à d’autres élèves de l’école ou d’ailleurs, aux familles, aux personnes âgées d’une résidence à proximité, aux enfants malades ou aux populations dans le besoin.</p>
                            <p>Tout peut devenir prétexte à réaliser un projet : développer de saines habitudes de vie, combattre l’intimidation, soulager la pauvreté ou l’exclusion sociale, réconforter, faire plaisir, émerveiller!</p>
                            <p>La recherche d’opportunités est à l’origine de tout projet qui doit évidemment développer l’esprit d’entreprendre des élèves, dont leurs qualités entrepreneuriales d’autonomie, de créativité, de ténacité, de prise de risques, de solidarité ou de leadership. Les enseignants disposent de nombreux outils pour planifier les projets (plan d’affaires, grille d’analyse de problème ou de besoin …) et apprécier les qualités entrepreneuriales développées par chacun. La Commission scolaire de l’Énergie se positionne avantageusement parmi les organisations les plus proactives en matière d’entrepreneuriat éducatif.</p>
                            <p>Chez nous, les directions d’école assument un beau leadership pédagogique qui facilite grandement le travail des enseignants et éducateurs. Ces derniers sont alimentés par la production de nombreux outils et chaque année, par des journées de formation qui propulsent la réalisation de projets dans nos écoles.</p>
                            <p>Cette année, le Concours québécois en entrepreneuriat, appelé maintenant OSEntreprendre, accueille 109 projets au primaire et au secondaire ainsi que, par un volet hors-concours, 11 projets réalisés en prématernelle et maternelle. Ces projets ont regroupé 2 278 élèves ce qui représente 26 % de toute la clientèle inscrite. La moyenne provinciale se situe à environ 8 à 10 %.</p>
                            <p>Vous pouvez avoir une idée, des projets et des thématiques qui stimulent les élèves entreprenants en visitant le site de la commission scolaire www.csenergie.qc.ca sous les onglets Primaire ou Secondaire dans la section entrepreneuriat.</p>
                            <p>Ce petit clin d’œil à l’entrepreneuriat dans les écoles démontre à quel point le goût d’entreprendre est à l’origine de l’esprit d’entreprise, et si tous ces jeunes ne s’orientent pas vers le démarrage de leur propre entreprise plus tard, gageons qu’ils auront une extraordinaire expérience de l’entrepreneuriat qui feront d’eux des intrapreneurs chers à nos organisations!</p>

