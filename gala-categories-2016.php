                            <!-- Single bloc categorie -->
                            <h4>Économie sociale :</h4>
                            <p>Catégorie discrétionnaire d’année en année. Un organisme à but non lucratif (OBNL) ayant des activités marchandes ou une coopérative offrant des biens ou services qui respecte les principes de fonctionnement suivants : un processus de gestion démocratique, la participation et la prise en charge individuelle et collective, la primauté de la personne et du travail sur le capital, et finalement, la propriété collective des capitaux et des moyens de production.</p>

                            <!-- Single bloc categorie -->
                            <h4>Relève entrepreneuriale :</h4>
                            <p>Catégorie discrétionnaire d’année en année. Entreprise ayant assuré sa pérennité grâce à un processus de transfert, de rachat, de fusion, d’acquisition ou de relève, et ce, depuis au plus deux (2) ans.</p>

                            <!-- Single bloc categorie -->
                            <h4>Personnalité d'affaires féminine :</h4>
                            <p class="ul-minus-margin-bottom">La personnalité d’affaires féminine se distingue par ses compétences entrepreneuriales et ses réalisations dans son secteur d’activités, son dynamisme et son engagement social et dans son milieu. La personnalité d’affaires féminine est choisie en fonction des éléments suivants :</p>

                                <!-- Bullet list -->
                                <ul>
                                    <li>Notoriété</li>
                                    <li>Leadership ou entrepreneurship</li>
                                    <li>Audace, ténacité, vision</li>
                                    <li>Engagement au sein de la communauté</li>
                                </ul>

                            <!-- Single bloc categorie -->
                            <h4>Personnalité d'affaires masculine :</h4>
                            <p class="ul-minus-margin-bottom">La personnalité d’affaires masculine se distingue par ses compétences entrepreneuriales et ses réalisations dans son secteur d’activités, son dynamisme et son engagement social et dans son milieu. La personnalité d’affaires masculine est choisie en fonction des éléments suivants :</p>

                                <!-- Bullet list -->
                                <ul>
                                    <li>Notoriété</li>
                                    <li>Leadership ou entrepreneurship</li>
                                    <li>Audace, ténacité, vision</li>
                                    <li>Engagement au sein de la communauté</li>
                                </ul>

                            <!-- Single bloc categorie -->
                            <h4>Diversification économique :</h4>
                            <p>Cette reconnaissance spéciale et publique met en lumière la contribution exceptionnelle d’entreprises de Shawinigan qui ont contribué aux efforts de diversification de l’économie de la ville, soit par son développement ou dans le contexte de démarrage d’une entreprise ou d’un nouveau projet. Cette reconnaissance est décernée par le Comité de diversification et de développement économique de la Ville de Shawinigan.</p>

                            <!-- Single bloc categorie -->
                            <h4>Prix du jury :</h4>
                            <p>Catégorie discrétionnaire d’année en année. Ce prix est décerné par le jury de sélection à une entreprise ou un entrepreneur s’étant distingué par l’excellence générale de ses performances ou par un succès exceptionnellement méritoire.</p>

                            <!-- Single bloc categorie -->
                            <h4>Prix Coup de coeur du public :</h4>
                            <p>Parmi les entreprises lauréates de la soirée, les participants du Gala distinction Desjardins seront appelés à sélectionner l’entreprise s’étant distinguée par l’excellence générale de ses performances ou par un succès exceptionnellement méritoire. (Les personnalités d’affaires ne sont pas admissibles.)</p>                                                                                                                

